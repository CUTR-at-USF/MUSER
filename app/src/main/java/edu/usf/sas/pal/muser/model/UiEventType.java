package edu.usf.sas.pal.muser.model;

/**
 * The enumerator that stores different types of user interface events triggered by the user
 * on a sound track.
 */
public enum UiEventType {
         PLAY,
         PLAY_ALBUM,
         PLAY_ALBUM_ARTIST,
         PLAY_GENRE,
         PAUSE,
         NEXT,
         PREV,
         REPEAT_OFF,
         REPEAT_ALL_SONGS,
         REPEAT_CURRENT_SONG,
         FAVORITE,
         UNFAVORITE,
         SEEK,
         CREATE_PLAYLIST,
         SELECT_CATEGORY,
         SELECT_ARTIST
}
